def _score_keywords(text: str, keywords: List[str]) -> int:
    text_l = (text or "").lower()
    return sum(1 for kw in keywords if kw.lower() in text_l)

def _rank(rule_dict: Dict[str, List[str]], text: str, limit: int = 3) -> List[dict]:
    scored = []
    for name, kws in rule_dict.items():
        hits = _score_keywords(text, kws)
        if hits:
            score = min(100, int(45 + hits * 18))
            scored.append({"name": name, "score": score, "evidence": [kw for kw in kws if kw.lower() in text.lower()][:5]})
    return sorted(scored, key=lambda x: x["score"], reverse=True)[:limit]

def detect_maturity(text: str) -> dict:
    best = {"name": "Не определено", "score": 0, "evidence": []}
    for name, kws in MATURITY_RULES:
        hits = _score_keywords(text, kws)
        if hits:
            score = min(100, int(45 + hits * 18))
            if score >= best["score"]:
                best = {"name": name, "score": score, "evidence": [kw for kw in kws if kw.lower() in text.lower()][:5]}
    return best

def profile_project(project: dict) -> dict:
    text = " ".join(str(project.get(k, "")) for k in ["title", "description", "abstract", "topic", "tags"])
    return {
        "technology_domains": _rank(DOMAIN_RULES, text),
        "business_model": _rank(BUSINESS_RULES, text, limit=2),
        "maturity_level": detect_maturity(text),
        "strategic_alignment": _rank(SOVEREIGNTY_RULES, text, limit=3),
    }
